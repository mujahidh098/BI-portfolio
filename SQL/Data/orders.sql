CREATE TABLE Orders (
  id INT NOT NULL AUTO_INCREMENT,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  orderNumber INT DEFAULT NULL,
  status ENUM('COMPLETED','ABANDONED','REFUNDED','VOIDED') DEFAULT NULL,
  subtotal DECIMAL(12,2) DEFAULT NULL,
  deliveryFee DECIMAL(12,2) DEFAULT NULL,
  discount DECIMAL(12,2) DEFAULT NULL,
  discountDescription VARCHAR(255) DEFAULT NULL,
  discountCode VARCHAR(255) DEFAULT NULL,
  total DECIMAL(12,2) DEFAULT NULL,
  source VARCHAR(255) DEFAULT NULL,
  uuid CHAR(36) CHARACTER SET latin1 COLLATE latin1_bin DEFAULT NULL UNIQUE,
  storeId INT DEFAULT NULL,
  shiftNumber VARCHAR(255) DEFAULT NULL,
  channelCode ENUM('COUNTER','CALL_IN') NOT NULL DEFAULT 'COUNTER',
  channelGroupCode ENUM('IN_STORE','3RD_PARTY','CALL_ORDER') NOT NULL,
  serviceTypeCode ENUM('TAKE_AWAY','DINE_IN') NOT NULL,
  tendered DECIMAL(12,2) NOT NULL DEFAULT '0.00',
  changeAmt DECIMAL(12,2) NOT NULL DEFAULT '0.00',
  tip DECIMAL(12,2) NOT NULL DEFAULT '0.00',
  driverTip DECIMAL(12,2) NOT NULL DEFAULT '0.00',
  outstanding DECIMAL(12,2) NOT NULL DEFAULT '0.00',
  orderRef VARCHAR(255) DEFAULT NULL,
  invoiceNumber VARCHAR(100) DEFAULT NULL,
  PRIMARY KEY (id),
  KEY Order_createdAt (createdAt)
) ENGINE=InnoDB AUTO_INCREMENT=9711 DEFAULT CHARSET=latin1; 