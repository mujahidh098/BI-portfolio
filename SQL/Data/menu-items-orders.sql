CREATE TABLE OrderMenuItems (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  orderId INT NOT NULL,
  itemId INT UNSIGNED NOT NULL,
  total DECIMAL(12,2) DEFAULT NULL,
  note VARCHAR(255) DEFAULT NULL,
  effectiveTax DECIMAL(10,2) NOT NULL DEFAULT '0.00',
  categoryId INT NOT NULL,
  PRIMARY KEY (id),
  KEY orderId (orderId),
  KEY itemId (itemId),
  KEY categoryId (categoryId),
  CONSTRAINT OrderMenuItems_ibfk_1 FOREIGN KEY (orderId) REFERENCES Orders (id) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT OrderMenuItems_ibfk_2 FOREIGN KEY (itemId) REFERENCES MenuItems (id) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT OrderMenuItems_ibfk_3 FOREIGN KEY (categoryId) REFERENCES MenuCategories (id) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=20753 DEFAULT CHARSET=latin1;
