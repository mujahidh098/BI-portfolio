CREATE TABLE StockItems (
  id int unsigned NOT NULL AUTO_INCREMENT,
  stockItemExternalId bigint unsigned DEFAULT NULL,
  stockCode varchar(255) NOT NULL,
  description varchar(255) DEFAULT NULL,
  quantityAvailable decimal(12,3) DEFAULT NULL,
  wastePercentage decimal(4,2) NOT NULL,
  manufactured tinyint(1) NOT NULL DEFAULT '0',
  controllable tinyint(1) NOT NULL DEFAULT '0',
  isEnabled tinyint(1) NOT NULL DEFAULT '1',
  createdAt datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt datetime DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  stockCategoryId bigint unsigned DEFAULT NULL,
  unitTypeId int NOT NULL,
  createdBy int DEFAULT NULL,
  preferredUnitTypeId int DEFAULT NULL,
  isReportItem tinyint(1) DEFAULT '0',
  threshold int DEFAULT '0',
  showThresholdOnPosApp tinyint(1) NOT NULL DEFAULT '0',
  nonDeductable tinyint(1) NOT NULL DEFAULT '0',
  isRestaurantSpecific tinyint(1) DEFAULT '0',
  stockLevelStatus enum('DEPLETED','CRITICALLY_LOW','LOW','OK') DEFAULT 'DEPLETED',
  allowOverrides tinyint NOT NULL DEFAULT '0',
  deletePending tinyint NOT NULL DEFAULT '0',
  isDeleted tinyint NOT NULL DEFAULT '0',
  PRIMARY KEY (id),
  UNIQUE KEY stockCode (stockCode),
  UNIQUE KEY stockItemExternalId (stockItemExternalId),
  KEY stockCategoryId (stockCategoryId),
  KEY unitTypeId (unitTypeId),
  KEY createdBy (createdBy),
  CONSTRAINT fk_StockItems_ibfk_1 FOREIGN KEY (stockCategoryId) REFERENCES StockCategories (externalID),
  CONSTRAINT StockItems_ibfk_2 FOREIGN KEY (unitTypeId) REFERENCES UnitTypes (id) ON UPDATE CASCADE,
  CONSTRAINT StockItems_ibfk_5 FOREIGN KEY (createdBy) REFERENCES Users (id) ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=4676 DEFAULT CHARSET=latin1;


